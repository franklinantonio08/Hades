      {{-- Body --}}
      <div class="modal-body small text-secondary">

        <p class="fw-bold text-dark mb-2">DECLARACIÓN JURADA</p>

        <p>
          El <strong>Registro Único de Extranjería (RUEX)</strong> constituye la identificación numérica
          permanente, única y oficial de toda persona extranjera que aplique a cualquiera de los
          procedimientos migratorios establecidos en el marco legal vigente de la República de Panamá.
        </p>

        <p>
          El RUEX es administrado, tratado y custodiado por el <strong>Servicio Nacional de Migración</strong>,
          bajo estrictas normas de confidencialidad, conforme a la Legislación Migratoria vigente y a la
          <strong>Ley de Protección de Datos Personales de la República de Panamá</strong>.
        </p>

        <hr>

        <p class="fw-semibold text-dark mb-2">
          Autorización para el tratamiento de datos personales
        </p>

        <p>
          El titular de los datos autoriza de manera expresa al Servicio Nacional de Migración para la
          administración, tratamiento y custodia de su información biográfica y biométrica. Dicha
          información podrá ser suministrada únicamente:
        </p>

        <ul class="mb-3">
          <li>Por solicitud del titular de los datos</li>
          <li>Por requerimiento de autoridad competente,</li>
        </ul>

        <p>
          Todo lo anterior, conforme a lo establecido en el <strong>Decreto Ley N.º 3 de 2008</strong> y las
          normas vigentes en materia de protección de datos personales.
        </p>

        <hr>

        <p class="fw-semibold text-dark mb-2">
          Uso de la información
        </p>

        <p>
          El titular declara conocer y aceptar que la información contenida en el RUEX podrá ser utilizada
          exclusivamente para:
        </p>

        <ul class="mb-3">
          <li>Fines estadísticos</li>
          <li>Fines de seguridad</li>
          <li>Verificación de identidad ante terceros debidamente autorizados,</li>
        </ul>

        <p>
          Siempre dentro del marco de la finalidad específica para la cual se realice dicha verificación.
        </p>

        <hr>

        <p class="fw-semibold text-dark mb-2">
          Naturaleza del número RUEX
        </p>

        <p>
          El número RUEX es de carácter <strong>único, permanente e invariable</strong>, sin posibilidad de
          repetición ni modificación. Mediante este número se identificará de forma individual a la
          persona extranjera registrada en el país.
        </p>

        <p>
          El Registro Único de Extranjería contendrá la información suministrada mediante la
          <strong>Declaración Jurada de Antecedentes Personales</strong>, la cual deberá mantenerse
          actualizada a través de los medios que para tal fin disponga el Servicio Nacional de Migración.
        </p>

        <hr>

        <p class="fw-semibold text-dark mb-2">
          Obligación de actualización de datos
        </p>

        <p>
          Toda persona extranjera está obligada a comunicar cualquier cambio en la información
          suministrada al Registro Único de Extranjería, conforme a lo establecido en el
          <strong>Artículo 37 del Decreto Ejecutivo N.º 3 de 2008</strong>.
        </p>

        <hr>

        <p class="fw-semibold text-dark mb-2">
          Verificación de información
        </p>

        <p>
          El titular autoriza al Servicio Nacional de Migración para verificar sus datos personales con el
          fin de constatar la veracidad de su identidad, así como su récord policivo y/o historial judicial
          ante autoridades nacionales, extranjeras o entidades internacionales de seguridad, asumiendo
          las consecuencias legales que dichas verificaciones puedan acarrear conforme a la legislación
          vigente.
        </p>

        <hr>

        <p class="fw-semibold text-dark mb-2">
          Declaración de veracidad
        </p>

        <p>
          Los datos suministrados se consideran ciertos y declarados de buena fe. La falsedad u omisión
          de información podrá dar lugar a <strong>responsabilidades administrativas y penales</strong>,
          conforme a la normativa aplicable.
        </p>

        <hr>

        <p class="fw-semibold text-dark mb-2">
          Inscripción presencial
        </p>

        <p class="mb-0">
          En caso de que sea la primera vez que se complete o actualice el RUEX, el titular deberá acudir
          personalmente a la sede principal del Servicio Nacional de Migración o a cualquiera de sus
          sedes regionales para completar su inscripción.
        </p>

