          <i class="bi bi-shield-lock me-2"></i>
          Declaración y Términos del Registro Único de Extranjería (RUEX)
        </h5>
        <button type="button" class="btn-close btn-close-white" data-bs-dismiss="modal" aria-label="Cerrar"></button>
